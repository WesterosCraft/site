const sanityRichTextToPayloadRichText = (sRichText: typeof test.entry) => {
  const newRT = [] as any[]

  const childrenMap = (children: any[]) => {
    const newChild = [] as any[]

    children.map(child => {
      if (child.marks.includes('strong')) {
        newChild.push({
          bold: true,
          text: child?.text,
        })
      } else if (child.marks.includes('underline')) {
        newChild.push({
          underline: true,
          text: child?.text,
        })
      } else {
        newChild.push({
          text: child?.text,
        })
      }
    })

    return newChild
  }

  sRichText.map(rt => {
    if (rt._type === 'block') {
      // HEADINGS
      if (
        rt.style === 'normal' ||
        rt.style === 'h3' ||
        rt.style === 'h2' ||
        rt.style === 'h1' ||
        rt.style === 'h4' ||
        rt.style === 'h5' ||
        (rt.style === 'h6' && !rt.listItem)
      ) {
        newRT.push({
          children: childrenMap(rt.children),
          type: rt.style,
        })
      }
    }

    if (rt._type === 'video') {
      newRT.push({
        type: 'video',
        id: rt?.url?.split('/')?.pop(),
        source: 'youtube',
        children: [
          {
            text: ' ',
          },
        ],
      })
    }
  })

  return newRT
}

